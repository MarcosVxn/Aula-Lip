public class Variaveis {
    public static void main(String[] args) {
        // Declara duas variaveis do tipo inteiro
        int var1;
        int var2;
        
        var1 = 1023;          // = é o operador recebe
        var2 = var1 / 2;     // operação aritmética
        // concatena texto com o valor da variável
        System.out.println("var1: " + var1);
        System.out.println("var2: " + var2);

    }
}

